namespace OpenSSH_GUI.Dialogs.Enums;

/// <summary>
///     Specifies the icon displayed in a <see cref="MessageBoxDialog" />.
/// </summary>
public enum MessageBoxIcon
{
    /// <summary>No icon is shown.</summary>
    None,

    /// <summary>An information icon (ℹ).</summary>
    Information,

    /// <summary>A warning icon (⚠).</summary>
    Warning,

    /// <summary>An error icon (✖).</summary>
    Error,

    /// <summary>A question icon (?).</summary>
    Question
}